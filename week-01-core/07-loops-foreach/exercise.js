// ==========================================
// โจทย์ข้อที่ 1: คำนวณยอดรวมโบนัส (for...of)
// ==========================================
const bonuses = [5000, 12000, 8500, 4000];
let totalBonus = 0;

// 🎯 สิ่งที่ต้องทำ:
// จงใช้ลูปแบบ for...of ในการวนลูปเพื่อนำค่าโบนัสในอาร์เรย์ bonuses มารวมกันไว้ในตัวแปร totalBonus
// จากนั้นพิมพ์คำว่า "รวมโบนัสทั้งหมดที่ต้องจ่าย: [totalBonus] บาท"
// เขียนโค้ดของคุณด้านล่างนี้:

for (const bonus of bonuses) {
  totalBonus += bonus;
}
console.log("🚀 ~ totalBonus:", totalBonus);

// ==========================================
// โจทย์ข้อที่ 2: สรุปรายงานกองทุน (forEach)
// ==========================================
const funds = ["KT-PRECIOUS", "K-CHG", "TMBGQG"];

// 🎯 สิ่งที่ต้องทำ:
// จงใช้เมธอด .forEach() ในการวนลูปอาร์เรย์ funds เพื่อแสดงข้อความรายงานออกหน้าจอทีละแถว
// ให้แสดงในรูปแบบ: "ลำดับที่ [เลข index + 1]: กองทุน [ชื่อกองทุน]"
// ตัวอย่างผลลัพธ์ที่ต้องการ:
// ลำดับที่ 1: กองทุน KT-PRECIOUS
// ลำดับที่ 2: กองทุน K-CHG
// เขียนโค้ดของคุณด้านล่างนี้:

funds.forEach((fundname, index) => {
  console.log(`ลำดับที่ ${index + 1}: กองทุน ${fundname}`);
});

// ==========================================
// โจทย์ข้อที่ 3: ระบบดักจับแฮกเกอร์ (ดึงพลังแบบดั้งเดิม)
// ==========================================
const logs = ["user_login", "user_click", "hack_attempt", "user_logout"];

// 🎯 สิ่งที่ต้องทำ:
// จงใช้ standard `for` loop หรือ `for...of` ก็ได้ วนลูปตรวจเช็คข้อความในอาร์เรย์ logs ทีละตัว
// - ถ้าเจอคำว่า "hack_attempt" ให้สั่ง console.log("🚨 ตรวจพบการแฮก! หยุดระบบทันที")
//   แล้วให้ใช้คำสั่งหยุดการทำงานของลูป (break) ทันที รอบหลังจากนั้นห้ามทำงานต่อ
// - ถ้าเจอคำอื่น ให้พิมพ์ปกติ เช่น "ตรวจสอบสถานะ: user_login"
// เขียนโค้ดของคุณด้านล่างนี้:

for (const log of logs) {
  if (log === "hack_attempt") {
    console.log("🚨 ตรวจพบการแฮก! หยุดระบบทันที");
    break;
  } else {
    console.log("ปกติ");
  }
}
