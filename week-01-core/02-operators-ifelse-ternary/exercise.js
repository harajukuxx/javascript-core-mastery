// โจทย์ข้อที่ 1: ตรวจสอบเลขคู่ หรือ เลขคี่
// - ประกาศตัวแปร let number = 15;
// - เขียนเงื่อนไข If/Else เพื่อตรวจสอบว่าตัวแปร number เป็นเลขคู่หรือเลขคี่
// - ถ้าเป็นเลขคู่ให้แสดงผล "เลขคู่" ถ้าเป็นเลขคี่ให้แสดงผล "เลขคี่"
// (ทริค: เลขคู่คือเลขที่หาร 2 แล้วเหลือเศษ 0)
// เขียนโค้ดด้านล่างนี้:

let number = 15;
if (number % 2 === 0) {
  console.log("เลขคู่");
} else {
  console.log("เลขคี่");
}

// โจทย์ข้อที่ 2: ใช้ Ternary Operator แปลงระบบจอดรถ
// - ประกาศตัวแปร const isMember = true;
// - ใช้ Ternary Operator เพื่อกำหนดค่าให้ตัวแปรชื่อ parkingFee
// - โดยมีเงื่อนไขว่า: ถ้าเป็นสมาชิก (isMember เป็น true) ค่าจอดรถจะเป็น 0 บาท แต่ถ้าไม่เป็นสมาชิก ค่าจอดรถจะเป็น 50 บาท
// - แสดงผลค่าจอดรถด้วย Template Literal
// เขียนโค้ดด้านล่างนี้:

const isMember = true;
const parkingFee = isMember ? 0 : 50;
console.log(`ค่าจอดรถของคุณคือ ${parkingFee} บาท`);
